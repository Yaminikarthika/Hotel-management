package com.hotel.rooms.model;

import org.springframework.stereotype.Component;

@Component(value="hotel")
public class Hotel {

	int id;
	String name;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
